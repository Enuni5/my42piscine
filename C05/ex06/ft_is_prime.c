int	ft_is_prime(int nb)
{
	int	i;
	int	j;

	i = 1;
	j = 1;
	while (i < nb)
	{
		while (j < nb)
		{
			if (i * j == nb)
				return (0);
			j++;
		}
		j = 1;
		i++;
	}
	return (1);
}
