#include <stdio.h>
#include <unistd.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

int	main(void)
{
	int	x;

	x = ft_strlen("buenas tardes hola que tal");
	printf("%d", x);
	return (0);
}
